#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*tmp;

	tmp = malloc(nmemb * size);
	if (!tmp)
		return (0);
	ft_memset(tmp, 0, nmemb * size);
	return (tmp);
}
